-- =====================================================
-- V1.0.6: 明确出发地和目的地字段语义
--
-- 背景：团建场景中，"出发地"和"目的地"是两个不同的概念：
-- - departure_city：出发城市，团队从哪里出发（如公司所在地：上海市）
-- - destination：目的地，团建活动举办地点（如：杭州千岛湖）
--
-- 本迁移通过 COMMENT 明确字段语义，避免开发时产生混淆
-- =====================================================

-- 1. plan_requests 表：明确字段注释
ALTER TABLE plan_requests
    MODIFY COLUMN departure_city VARCHAR(50) NOT NULL
        COMMENT '出发城市（团队从哪里出发，如公司所在地：上海市）',
    MODIFY COLUMN destination VARCHAR(100) DEFAULT NULL
        COMMENT '目的地（团建活动举办地点，如：杭州千岛湖）';

-- 2. plans 表：明确字段注释
ALTER TABLE plans
    MODIFY COLUMN departure_city VARCHAR(100) DEFAULT NULL
        COMMENT '出发城市（团队从哪里出发，如公司所在地：上海市）',
    MODIFY COLUMN destination VARCHAR(100) DEFAULT NULL
        COMMENT '目的地（团建活动举办地点，如：杭州千岛湖）';

-- =====================================================
-- 字段语义对照表（供开发参考）
-- =====================================================
--
-- | 字段名          | 中文名   | 语义说明                              | 示例值        |
-- |----------------|---------|--------------------------------------|--------------|
-- | departure_city | 出发城市 | 团队出发地点，通常是公司所在城市         | 上海市        |
-- | destination    | 目的地   | 团建活动举办地点，团队前往的地方         | 杭州千岛湖    |
--
-- 前端显示格式："{departure_city} → {destination}"
-- 示例：上海市 → 杭州千岛湖
--
-- =====================================================
